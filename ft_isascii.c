int	ft_isascii(int c)
{
	if (c < 0 || c > 127)
		return (0);
	return (1);
}
#include <stdio.h>


int main()
{
	int a;
	a = -1;
	while (a <= 128)
	{	
		printf("%d %d\n", isascii(a), a);
		a++;
	}
}
